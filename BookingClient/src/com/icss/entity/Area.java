package com.icss.entity;

/**
 * 地区实体类.
 * @author mdx
 *
 */
public class Area {

	private int ano;      //地区编号
	private String aname; //地区名字
	
	
	
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public String getAname() {
		return aname;
	}
	public void setAname(String aname) {
		this.aname = aname;
	}
	
	
	
	
}
